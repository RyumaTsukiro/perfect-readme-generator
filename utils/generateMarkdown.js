// Fungsi untuk membuat lencana lisensi
const renderLicenseBadge = (license) => {
  if (license !== 'None') {
    return `[![License](https://img.shields.io/badge/License-${license}-yellow.svg)](https://opensource.org/licenses/${license.replace('_', '-')})`;
  }
  return '';
};

// Fungsi untuk membuat link lisensi
const renderLicenseLink = (license) => {
  if (license !== 'None') {
    return `\n* [License](#license)\n`;
  }
  return '';
};

// Fungsi untuk membuat bagian lisensi
const renderLicenseSection = (license) => {
  if (license !== 'None') {
    return `## License

This project is licensed under the ${license.replace('_', ' ')} License.`;
  }
  return '';
};

// Fungsi utama untuk menghasilkan markdown
